#colocamos números em '' quando queremos que ele seja um texto, pois números não precisam estar em aspas

#integer - numeros interos
print(123 + 134) #aqui ele soma normal por se tratar de números

#string
print('123' + '123') #aqui ele junta os textos por estar sendo passado como string, chamado também de concatenação (juntar 2 textos)

#large integers - inteiros grandes, longos sem casas decimais
print(123_456_456) #passamos entre eles _ ao inves de vitgulas para que ele entenda que é um número considerado large intergers e não interfere na nossa visualizaçõ, pois oculta o _

#float - floating point number - numeros flutuantes que podem mudar o ponto de onde estão e mudar o número como lemos 346.90 pode virar 3.4690
print(3.73673467)

#boolean - boleano - True or False - sempre com letra maiscula 
print(True)
print(False)